public class PasaporteSanitario {
	private String fecha_pcr;
	private boolean resultado_pcr;

	public String getFecha_pcr() {
		return fecha_pcr;
	}

	public void setFecha_pcr(String fecha_pcr) {
		this.fecha_pcr = fecha_pcr;
	}

	public boolean isResultado_pcr() {
		return resultado_pcr;
	}

	public void setResultado_pcr(boolean resultado_pcr) {
		this.resultado_pcr = resultado_pcr;
	}
}